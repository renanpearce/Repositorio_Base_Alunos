# condicional simples
# para condições em python utilizamos a palavra if que significa 
# 'se' em portugues. 
# com a condicional, voce consegue executar um cojunto 
# de codigo APENAS se o resultado  da expressão  for "verdadeiro" (true)
idade  = int  ( input ("qual a sua idade ?"))
if idade  >= 18: 
    print ("maior de idade")
else:
    print ("menor de idade")