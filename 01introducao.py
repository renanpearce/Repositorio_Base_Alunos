#como criar uma lista?

#usamos colchetes [ ] para defenir uma lista, separando os elementos por virgulas.
# nome_da_lista = [elemento_0, elemento_1, elemento_2]

diversos = [1,"banana", "mouse", 23.5, "aeiou", True, [1,2,3,5]]
# isso mostra que listas são flexíveis e podem misturar tipos de dados.

print(diversos) # lista total
print(diversos[3]) # a lista começa com indice
print(diversos[-1]) # pegar o último elemento,
print(diversos[6]) # pegar um elemento especifico
print(diversos[-1][2]) # pegar um elemento da lista dentro de outra lista



